class Punto
    #Constructor
    def initialize(valorX, valorY)
        @x = valorX
        @y = valorY
    end

    #MEtodos get
    def getX()
        return @x
    end

    def getY()
        return @y
    end

    #MEetodos SEt
    def setX(valorX)
        @x=valorX
    end
    
    def setY(valorY)
        @y=valorY
    end

end
